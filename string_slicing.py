"""
string slicing means to manipulate a string.
"""
food="rice"

#to print the first element in the string
print(food[0])

#print the third element
print(food[2])

#print the last element
print(food[3])
print(food[-1])

#slice from 2nd to last
print(food[1:])


# to reverse a string
print(food[::-1])

#slice out the whole string
print(food[:])

#slice from the 1st to the 3rd
print(food[0:3])

# skip the 2nd element
print(food[0:-1:1])